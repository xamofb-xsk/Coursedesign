class Student:
    __sid = ""
    __sname = ""
    __sphone = ""
    __saddress = ""

    def setSid(self, sid):
        self.__sid = sid

    def getSid(self):
        return self.__sid

    def setSname(self, sname):
        self.__sname = sname

    def getSname(self):
        return self.__sname

    def setSphone(self, sphone):
        self.__sphone = sphone

    def getSphone(self):
        return self.__sphone

    def setSaddress(self, saddress):
        self.__saddress = saddress

    def getSaddress(self):
        return self.__saddress


